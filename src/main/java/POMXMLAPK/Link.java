package POMXMLAPK;

public class Link {
private String agentName;
private int akym;
	public Link(String agentName, int akym ) {
		this.agentName = agentName;
		this.akym = akym;
		
	}
	public Link() {
		
	}
	public String getAgentName() {
		return agentName;
	}
	public void setAgentName(String agentName) {
		this.agentName = agentName;
	}
	public int getAkym() {
		return akym;
	}
	public void setAkym(int akym) {
		this.akym = akym;
	}

	

}
